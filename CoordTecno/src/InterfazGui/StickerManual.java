    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
        JFileChooser jf = new JFileChooser();
        jf.showOpenDialog(this);
        File archivo = jf.getSelectedFile();
        if(archivo != null){
            txtRuta.setText(archivo.getAbsolutePath());
        }

        File file = new File(txtRuta.getText());
        //ArrayList listaGuia= new ArrayList<>();
        Scanner scanner;
        try {
            //se pasa el flujo al objeto scanner
            scanner = new Scanner(file);
            while (scanner.hasNextLine()) {
                // el objeto scanner lee linea a linea desde el archivo
                String linea = scanner.nextLine();
                Scanner delimitar = new Scanner(linea);
                //se usa una expresiÃ³n regular
                //que valida que antes o despues de una TABULACION (\t) exista cualquier cosa
                //parte la cadena recibida cada vez que encuentre una TABULACION
                delimitar.useDelimiter("\t");
                Guia2 e= new Guia2(linea, linea, linea, linea,linea, 0);
                e.setGuia(delimitar.next());
                e.setRemitente(delimitar.next());
                e.setDestinatario(delimitar.next());
                e.setDir(delimitar.next());
                e.setCiudad(delimitar.next());
                e.setUnidades(delimitar.nextInt());
                //listaGuia.add(e);

                Object datos[]={e.getGuia(),e.getRemitente(),e.getDestinatario(),
                    e.getDir(),e.getCiudad(),e.getUnidades()};
                md.addRow(datos);
            }
            //se cierra el ojeto scanner
            scanner.close();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnSelecionarActionPerformed

